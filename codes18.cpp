#include<iostream>
using namespace std;
int main(){
    // bitwise operator
    int a=5;
    int b=3;
    // AND
    cout<<"a&b= "<<(a&b)<<endl;
    // OR
    cout<<"a|b= "<<(a|b)<<endl;
    // XOR
    cout<<"a^b= "<<(a^b)<<endl;
    // NOT
    cout<<"~a= "<<(~a)<<endl;
    // left shift
    cout<<"a<<1= "<<(a<<1)<<endl;
    // right shift
    cout<<"a>>1= "<<(a>>1)<<endl;
    return 0;

}